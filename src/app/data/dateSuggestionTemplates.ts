/**
 * Date Suggestion Templates
 *
 * Templates for personalized date ideas organized by:
 * - Date Style (from onboarding preferences)
 * - Effort Level
 * - Budget Level
 * - Indoor/Outdoor
 * - Time Required
 *
 * Placeholders for personalization:
 * - {partner_name} - Partner's name
 * - {favorite_activity} - Extracted from saved insights
 * - {mentioned_place} - Specific location mentioned
 * - {season} - Current season
 */

import type { PlaceCategory } from '../services/nearbyPlacesService';

export type DateStyle = 'relaxed' | 'adventurous' | 'cultural' | 'foodie' | 'at_home';
export type DateType = 'picnic' | 'dinner' | 'movie_concert' | 'hiking' | 'museum' | 'cooking' | 'game_night' | 'at_home_spa';
export type EffortLevel = 'Low' | 'Medium' | 'High';
export type BudgetLevel = '$' | '$$' | '$$$';
export type Environment = 'indoor' | 'outdoor' | 'both';
export type LoveLanguage = 'Words of Affirmation' | 'Quality Time' | 'Acts of Service' | 'Receiving Gifts' | 'Physical Touch';

export interface DateTemplate {
  id: string;
  title: string;
  description: string;
  category: 'date';
  dateStyle: DateStyle[];
  dateType: DateType;
  loveLanguage: LoveLanguage[];
  budget: BudgetLevel;
  effort: EffortLevel;
  environment: Environment;
  timeRequired: string;
  bestSeason?: string[];
  requiresData: string[];
  optionalData: string[];
  personalizationTier: 1 | 2 | 3 | 4;
  avoidIf: string[];
  sharedHobbies?: string[];
  growthAreas?: string[];
  travelAspirations?: string[];
  insideJokes?: string[];
  
  // Enhanced metadata for improved matching
  foodTypes?: string[]; // e.g., ['Italian', 'Asian', 'Mexican']
  activityTypes?: string[]; // e.g., ['outdoor', 'cultural', 'social']
  interestCategories?: string[]; // e.g., ['art', 'music', 'nature', 'food']
  
  // Venue Requirements
  requiredVenues?: PlaceCategory[]; // Required venue types (strict)
  optionalVenues?: PlaceCategory[]; // Optional venue types (flexible)
  minVenueDistance?: number; // Minimum distance in miles (for scenic drives)
  maxVenueDistance?: number; // Maximum distance in miles (default: 5)
  
  // Matching Metadata
  popularFor?: string[]; // Popular in these locations/regions
  seasonal?: {
    best: string[]; // Best seasons
    avoid: string[]; // Avoid these seasons
  };
  weatherDependent?: boolean; // Requires specific weather
  dayOfWeek?: string[]; // Best on these days ['monday', 'tuesday', etc.]
  
  // Quality Indicators
  popularityScore?: number; // 1-10 how popular this date type is
  uniquenessScore?: number; // 1-10 how unique/special
  effortLevel: EffortLevel; // Already have 'effort' field, keeping for compatibility
  
  // Interest Matching
  interestTags?: string[]; // e.g., ['art', 'coffee', 'nature', 'food']
  activityTags?: string[]; // e.g., ['walking', 'conversation', 'exploring']
  
  // Venue-Specific
  prefersHighRated?: boolean; // Prefer 4.5+ rated venues
  requiresPopular?: boolean; // Must be a popular destination
  allowsFreeVenues?: boolean; // Can include free parks/museums
  
  // Compatibility
  bestForCouples?: string[]; // e.g., ['new relationships', 'long-term']
  
  // Template Grouping and Fallback
  groupId?: string; // Links related template variations (e.g., all "Wine Tasting" variations)
  alternativeGroupIds?: string[]; // Alternative date idea groups if primary group doesn't match venues
  priority?: number; // Within a group, higher priority templates tried first (1-10, higher = better)
}

